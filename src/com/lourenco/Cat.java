package com.lourenco;

public class Cat {
    public String name;
    public int age;
    public String color;
    public boolean smells;


    public void setName(String name){
            this.name = name;
    }

    public String getName(){
        return name;
    }

    public void setAge(int age){
        this.age = age;
    }

    public int getAge(){
        return age;
    }

    public void setColor(String color){
        this.color = color;
    }

    public String getColor(){
        return color;
    }

    public void setSmells(boolean smells){
        this.smells = smells;
    }

    public boolean getSmells(){
        return smells;
    }

}
